package com.mycompany.n1;

/*Variables Primitivas: Declara e inicializa variables de cada 
tipo primitivo en Java (int, double, boolean, char, byte, short, long, float).*/
public class N1 {

    public static void main(String[] args) {
        int entero = 10;
        double doble = 10.5;
        boolean boleano = true;
        char caracter = 'a';
        byte byt = 1;
        short corto = 2;
        long largo = 3;
        float flotante = 4.5f;
        
        System.out.println("Entero: " + entero);
        System.out.println("Doble: " + doble);
        System.out.println("Booleano: " + boleano);
        System.out.println("Caracter: " + caracter);
        System.out.println("Byte: " + byt);
        System.out.println("Short: " + corto);
        System.out.println("Long: " + largo);
        System.out.println("Float: " + flotante);
    }
}
