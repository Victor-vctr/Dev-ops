package com.mycompany.n12;

/*12-Incremento y Decremento: Demuestra el uso de los operadores de incremento
(++) y decremento (--) en una variable.*/
public class N12 {

    public static void main(String[] args) {
        int entero = 10;
        System.out.println("Entero: " + entero);
        
        // Incremento
        entero++;
        System.out.println("Incremento: " + entero);
        
        // Decremento
        entero--;
        System.out.println("Decremento: " + entero);
    }
}
