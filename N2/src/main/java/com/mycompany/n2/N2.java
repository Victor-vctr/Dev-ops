package com.mycompany.n2;

/* 2-Constantes: Declara una constante de tipo int con el valor 
100 utilizando la palabra clave final.*/
public class N2 {

    public static void main(String[] args) {
        final int CONSTANTE = 100;
        System.out.println("Constante: " + CONSTANTE);
        
        final String CONSTANTE2 = "Hola";
        System.out.println("Constante: " + CONSTANTE2); 
    }
}
