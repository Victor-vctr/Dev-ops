package com.mycompany.n8;

/*8-Uso de Math: Utiliza Math.sqrt() para calcular la raíz cuadrada de
 un número y muestra el resultado.*/
public class N8 {

    public static void main(String[] args) {
        int num = 16;
        double raiz = Math.sqrt(num);
        System.out.println("Raíz cuadrada de " + num + ": " + raiz);
    }
}
