package com.mycompany.n13;

/*13-Asignación Compuesta: Usa operadores de asignación compuesta 
(como += y -=) en un ejemplo práctico.*/
public class N13 {

    public static void main(String[] args) {
        int entero = 10;
        System.out.println("Entero: " + entero);
        
        // Asignación compuesta de suma
        entero += 5;
        System.out.println("Suma: " + entero);
        
        // Asignación compuesta de resta
        entero -= 3;
        System.out.println("Resta: " + entero);
    }
}
