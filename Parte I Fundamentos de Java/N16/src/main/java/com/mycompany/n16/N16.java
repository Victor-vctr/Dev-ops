package com.mycompany.n16;

/*16-Declaración Múltiple: Inicializa varias variables en una sola línea y muéstralas por consola.*/
public class N16 {

    public static void main(String[] args) {
        // Declaración e inicialización múltiple
        int a = 10, b = 20, c = 30;
        double x = 5.5, y = 7.7, z = 9.9;
        String nombre = "Juan", apellido = "Pérez";

        // Mostrar los valores en la consola
        System.out.println("Valores de enteros: " + a + ", " + b + ", " + c);
        System.out.println("Valores de decimales: " + x + ", " + y + ", " + z);
        System.out.println("Nombre completo: " + nombre + " " + apellido);
    }
}
