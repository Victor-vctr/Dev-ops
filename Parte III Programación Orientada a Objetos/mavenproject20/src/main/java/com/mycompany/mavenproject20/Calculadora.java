package com.mycompany.mavenproject20;


public class Calculadora {
    // Suma de dos números enteros
    public int sumar(int a, int b) {
        return a + b;
    }

    // Suma de tres números enteros
    public int sumar(int a, int b, int c) {
        return a + b + c;
    }

    // Suma de dos números decimales (double)
    public double sumar(double a, double b) {
        return a + b;
    }

    // Suma de tres números decimales
    public double sumar(double a, double b, double c) {
        return a + b + c;
    }

    // "Suma" de dos cadenas (concatenación)
    public String sumar(String a, String b) {
        return a + b;
    }
}
